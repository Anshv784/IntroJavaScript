// String
let name = "John Doe";
console.log("String:", name);

// Number
let age = 30;
console.log("Number:", age);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Undefined
let address;
console.log("Undefined:", address);

// Null
let car = null;
console.log("Null:", car);

// Object
let person = {
    firstName: "Jane",
    lastName: "Doe",
    age: 25
};
console.log("Object:", person);

// Array
let colors = ["red", "green", "blue"];
console.log("Array:", colors);

// Function
function greet() {
    return "Hello, World!";
}
console.log("Function:", greet);